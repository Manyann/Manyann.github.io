  static getAllForLoot(): Record<ItemRarity, LootItem[]> {
    let items = this.getAll()
      .map((item) => ({
        name: item.libelle,
        rarity: this.getQualiteFromBaseChance(
          item.basePourcentage,
        ) as ItemRarity,
      }))
      .reduce(
        (acc, item) => {
          const key = item.rarity as ItemRarity;
          if (!acc[key]) {
            acc[key] = [];
          }
          acc[key].push(item);
          return acc;
        },
        {} as Record<ItemRarity, LootItem[]>,
      );

    return items;
  }

  static getQualiteFromBaseChance(chance: number) {
    if (chance <= 42) {
      return 'legendaire';
    } else if (chance <= 55) {
      return 'excellence';
    } else if (chance <= 70) {
      return 'artisant';
    } else if (chance <= 90) {
      return 'qualite';
    } else {
      return 'perave';
    }
  }

