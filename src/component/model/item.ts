  static getAllArmureForLoot(): Record<ItemRarity, LootItem[]> {
    let items = this.getAllArmure()
      .filter((x) => x.prix > 0)
      .map((item) => ({
        name: item.libelle,
        rarity: this.getQualiteFromBaseChance(
          item.basePourcentage,
        ) as ItemRarity,
      }))
      .reduce(
        (acc, item) => {
          const key = item.rarity as ItemRarity;
          if (!acc[key]) {
            acc[key] = [];
          }
          acc[key].push(item);
          return acc;
        },
        {} as Record<ItemRarity, LootItem[]>,
      );

    //items['mythique'].push({name:'???', rarity:'mythique'});

    return items;
  }

